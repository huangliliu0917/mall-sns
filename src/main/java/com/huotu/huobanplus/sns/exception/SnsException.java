package com.huotu.huobanplus.sns.exception;

/**
 * Created by jin on 2016/10/31.
 * 整个项目的总异常
 */
public class SnsException extends Exception {

    private int code;

    public SnsException(String message) {
        super(message);
    }

    public SnsException(int code, String message) {
        super(message);
        this.code = code;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
